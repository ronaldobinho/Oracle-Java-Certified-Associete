package aula3;

public class Programa01 {
	public static void main(String[] args) {
		// Operadores Unários
		
		//++c = Realiza a soma e imprime no novo valor
		//c++ = Imprime o valor e realiza a soma
		
		int c = 10;
		System.out.println(c);
		System.out.println(++c);
		System.out.println(++c);
	}
}
